      <h3 class="u-text u-text-default u-text-1">Notre Histoire</h3>
      <p class="u-text u-text-2">
        <span style="font-size: 1rem;">
          <span style="font-weight: 700;">Un chercheur en quête de solutions</span>
          <br>Inauguré en 2015, le <span style="font-weight: 700;" class="u-text-custom-color-3">CNRD</span> a reçu plus de 8000 patients au cours de ses six premiers mois. Le complexe est équipé de 23 lits, une banque de sang, un automate pour dépistage rapide, une pharmacie, une salle de radiologie, ainsi qu’une cafeteria adaptée aux besoins nutritionnels des drépanocytaires, et une guest house pour les chercheurs venus d’autres pays.<br>
          <br>« Le <span style="font-weight: 700;" class="u-text-custom-color-3">CNRD</span> a une vocation sous régionale et nous voulons en faire un centre d’excellence sur le plan de la recherche, du dépistage, du traitement et du suivi des personnes atteintes de drépanocytose », souligne le Pr <span style="font-weight: 700;">Alexis Elira Dokekias</span>, qui dirige la structure depuis son lancement.
        </span>
        <span style="font-size: 1rem;">
          <br>
          <br>Une étude épidémiologique est prévue à travers le pays au cours des 3 prochains mois, la première depuis 1984. Il est aussi prévu de mettre sur pied une banque de données génétiques et un diplôme de spécialisation sur la drépanocytose pour médecins et infirmiers à partir de l’année académique 2020 à l’université Marien Ngouabi de Brazzaville. Le Pr <span style="font-weight: 700;">Elira Dokekias</span> ambitionne également de faire des diagnostics périnatals afin de détecter la maladie le plus tôt que possible.Le CNRD a signé deux accords de partenariat avec le Howard University Center for Sickle Cell Disease (Centre sur la drépanocytose à l’Université de Howard), basé à Washington, aux États-Unis. Les deux institutions établiront bientôt un centre de formation en hématologie à Brazzaville. Howard a aussi aidé à recruter et à former une chercheuse congolaise pour faire la liaison sur les soins cliniques et la recherche entre les deux pays.« Nous espérons que la République du Congo sera le premier pays d'Afrique à avoir un programme de dépistage universel de la drépanocytose chez les nouveau-nés », déclare le Professeur James G. Taylor VI, directeur du Howard University Center for Sickle Cell Disease. « Pour ce qui est de la drépanocytose, les défis auxquels est confrontée l’Afrique sont comparables à ceux auxquels nous sommes confrontés aux Etats-Unis – à savoir, un manque de financement et un nombre insuffisant de personnel de santé qualifié », précise-t-il.Le professeur Taylor reste cependant optimiste. « En Afrique, d'importants alliés politiques ont attiré l'attention sur l'importance de cette maladie », observe-t-il.<br>
          <br>
          <span style="font-weight: 700;">Plaidoyer de la Première Dame</span>
          <br>Un de ces alliés est indubitablement la Première Dame du <span style="font-weight: 700;">Congo</span>, Mme <span style="font-weight: 700;">Antoinette Sassou Nguesso</span>, dont le <span style="font-weight: 700;" class="u-text-custom-color-3">CNRD</span>
          <span class="u-text-custom-color-3"></span>porte le nom. En effet à travers sa fondation <span style="font-weight: 700;">Congo Assistance</span>, elle est parvenue à mettre la drépanocytose sur l’agenda des décideurs congolais et à mobiliser des ressources pour le fonctionnement du <span style="font-weight: 700;" class="u-text-custom-color-3">CNRD</span>. Pour son attachement à la cause des drépanocytaires, Mme <span style="font-weight: 700;">Antoinette Sassou Nguesso</span>&nbsp;a été distinguée lors de la 72e Assemblée mondiale de la santé qui s’est tenue le mois de mai à Genève.L'<span style="font-weight: 700;">OMS </span>a apporté son expertise technique pour l'opérationnalisation du <span style="font-weight: 700;" class="u-text-custom-color-3">CNRD</span> et l'a doté d'un automate d'hématologie. Elle a par ailleurs apporté son appui technique et financier au renforcement des capacités des hôpitaux des districts sanitaires à travers le pays dans le dépistage et la prise en charge de la drépanocytose. Une mission conjointe de l’<span style="font-weight: 700;">OMS </span>et du ministère de la santé du Congo est prévue dans les prochains jours pour renforcer davantage les efforts consentis dans la lutte contre la <span style="font-weight: 700;">drépanocytose</span>. Grâce à tous ces efforts, l’espoir est désormais permis pour les personnes atteintes de <span style="font-weight: 700;">drépanocytose</span>.
        </span>
        <br>
      </p>
